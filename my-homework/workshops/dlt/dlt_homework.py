def square_root_generator(limit):
    n = 1
    while n <= limit:
        yield n ** 0.5
        n += 1

# Example usage:
limit = 13
generator = square_root_generator(limit)

values_sum = 0
for sqrt_value in generator:
    print(sqrt_value)
    values_sum += sqrt_value

print(values_sum)

