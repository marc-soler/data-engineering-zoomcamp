import dlt
import duckdb
from IPython.display import display

# Loading first generator
generators_pipeline = dlt.pipeline(destination="duckdb", dataset_name="generators")

info = generators_pipeline.run(
    people_1(), table_name="people", write_disposition="replace"
)

# Retrieving sum of ages from first generator
conn = duckdb.connect(f"{generators_pipeline.pipeline_name}.duckdb")

generator_1 = conn.sql("SELECT SUM(age) FROM generators.people").df()
display(generator_1)

# Loading second generator
